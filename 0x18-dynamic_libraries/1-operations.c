/**
 * add - function that add teo ints
 * @a: the first int
 * @b: the second int
 * @Return: sum ofthe ints
 */
int add(int a, int b)
{
	return (a + b);
}
/**
 * sub - function that subtructs oneint form another
 * @a: firts int
 * @b: second int
 * @Return: the difference of the ints
 */
int sub(int a, int b)
{
	return (a - b);
}
/**
 * mul - function that multiplies two ints
 * @a: the first int
 * @b: the second int
 * @Return: the product of the two ints
 */
int mul(int a, int b)
{
	return (a * b);
}
