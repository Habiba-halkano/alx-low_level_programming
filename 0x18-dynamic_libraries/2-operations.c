#include <stdio.h>
#include <stdlib.h>
/**
 * division - function that divides two ints
 * @a: the dividend
 * @b: the divisor
 * Return: the quotient of the division
 */
int division(int a, int b)
{
	return (a / b);
}
/**
 * mod - function that computes the modulo of one int with respect to another
 * @a: dividend
 * @b: the divisor
 */
int mod(int a, int b)
{
	return (a % b);
}
